			$subscription['currency'],
			$frequency,
			$subscription['trial_billing_cycle']
		);
	}
	$data['payment_description'] = sprintf(
		// Translators: 1: trial description, 2: recurring amount, 3: currency, 4: frequency, 5: cycles.
		_n(
			'%1$s%2$.2f %3$s every %4$s for %5$d installment',
			'%1$s%2$.2f %3$s every %4$s for %5$d installments',
			(int) $subscription['recur_billing_cycle'],
			'wishlist-member'
		),
		$trial,
		$subscription['recur_amount'],
		$subscription['currency'],
		$frequency,
		(int) $subscription['recur_billing_cycle']
	);

