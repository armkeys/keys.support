	$trial = '';
	if ((float) $subscription['trial_amount'] && (int) $subscription['trial_billing_cycle']) {
		$trial = sprintf(
			// Translators: 1: trial amount, 2: currency, 3: frequency, 4: trial cycles.
			_n(
				'%1$.2f %2$s every %3$s for %4$d installment <br> Then ',
				'%1$.2f %2$s every %3$s for %4$d installments <br> Then ',
				$subscription['trial_billing_cycle'],
				'wishlist-member'
			),
