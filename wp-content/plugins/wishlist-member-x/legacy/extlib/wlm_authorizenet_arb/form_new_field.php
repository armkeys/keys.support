		case 'd':
			// Translators: %d: frequency in Days.
			$frequency = _n( '%d Day', '%d Days', $subscription['recur_billing_frequency'], 'wishlist-member' );
			break;
		case 'w':
			// Translators: %d: frequency in Weeks.
			$frequency = _n( '%d Week', '%d Weeks', $subscription['recur_billing_frequency'], 'wishlist-member' );
			break;
		case 'm':
			// Translators: %d: frequency in d.
			$frequency = _n( '%d Month', '%d Months', $subscription['recur_billing_frequency'], 'wishlist-member' );
			break;
		case 'y':
			// Translators: %d: frequency in Years.
			$frequency = _n( '%d Year', '%d Years', $subscription['recur_billing_frequency'], 'wishlist-member' );
			break;
